export const PROPERTIES: Property[] = RAW_PROPERTIES.map((raw) => {
  const info = getMinpakuInfo(raw.zoning, raw.isTokkuArea);
  const minpakuType = getMinpakuBadgeType(info);
  return {
    ...raw,
    minpakuType,
    minpakuNote: info.note,
    maxDays: info.maxDays,
  };
});

export function searchProperties(params: {
  prefecture?: string;
  city?: string;
  minRent?: number;
  maxRent?: number;
  layout?: string;
  minpakuType?: MinpakuType | "ALL";
  keyword?: string;
  tags?: string[];
}): Property[] {
  return PROPERTIES.filter((p) => {
    if (params.prefecture && params.prefecture !== "ALL" && p.prefecture !== params.prefecture) return false;
    if (params.city && params.city !== "ALL" && !p.city.includes(params.city)) return false;
    if (params.minRent && p.rent < params.minRent) return false;
    if (params.maxRent && p.rent > params.maxRent) return false;
    if (params.layout && params.layout !== "ALL" && p.layout !== params.layout) return false;
    if (params.minpakuType && params.minpakuType !== "ALL" && p.minpakuType !== params.minpakuType) return false;
    if (params.tags?.length && !params.tags.every((tag) => p.tags.includes(tag))) return false;
    if (params.keyword) {
      const kw = params.keyword.toLowerCase();
      if (
        !p.title.toLowerCase().includes(kw) &&
        !p.address.toLowerCase().includes(kw) &&
        !p.city.toLowerCase().includes(kw) &&
        !p.nearestStation.toLowerCase().includes(kw)
      ) return false;
    }
    return true;
  });
}

export function getPropertyById(id: string): Property | undefined {
  return PROPERTIES.find((p) => p.id === id);
}

